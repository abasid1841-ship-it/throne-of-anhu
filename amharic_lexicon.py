def get_amharic_context(query: str) -> str:
    query_lower = query.lower()
    matches = []
    
    for term, info in AMHARIC_LEXICON.items():
        if term in query_lower:
            matches.append(f"**{term.title()}**: {info['meaning']}\n*Scroll Context*: {info['scroll_context']}\n*Usage*: {info['usage']}")
    
    if matches:
        return "AMHARIC LEXICAL CONTEXT:\n" + "\n\n".join(matches)
    return ""
